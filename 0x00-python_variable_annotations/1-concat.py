#!/usr/bin/env python3
""" Contains `concat` """


def concat(str1: str, str2: str) -> str:
    """ Concatenates two strings

    Args:
        str1 (str): _First string operand_
        str2 (str): _Second string operand_

    Returns:
        str: _concantenated string_
    """
    return str1 + str2
